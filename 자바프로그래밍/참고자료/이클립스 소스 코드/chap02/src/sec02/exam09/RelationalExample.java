package sec02.exam09;

public class RelationalExample {
    public static void main(String[] args) {
        int a = 10, b = 3;
       
        System.out.println("a == b: " + (a == b));  	// false 
        System.out.println("a != b: " + (a != b));  	// true
        System.out.println("a > b: " + (a > b));        // true
        System.out.println("a < b: " + (a < b));        // false
        System.out.println("a >= b: " + (a >= b));  	// true
        System.out.println("a <= b: " + (a <= b));  	// false
    }
}